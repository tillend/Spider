package com.lin.model;

import java.util.List;

public class AnimeListModel {
	
	private List<String> title;
	private List<String> url;
	private List<String> desc;
	private List<String> up_info;
	private List<String> watch_count;
	private List<String> dm_count;
	
	public List<String> getTitle() {
		return title;
	}
	public void setTitle(List<String> title) {
		this.title = title;
	}
	public List<String> getUrl() {
		return url;
	}
	public void setUrl(List<String> url) {
		this.url = url;
	}
	public List<String> getDesc() {
		return desc;
	}
	public void setDesc(List<String> desc) {
		this.desc = desc;
	}
	public List<String> getUp_info() {
		return up_info;
	}
	public void setUp_info(List<String> up_info) {
		this.up_info = up_info;
	}
	public List<String> getWatch_count() {
		return watch_count;
	}
	public void setWatch_count(List<String> watch_count) {
		this.watch_count = watch_count;
	}
	public List<String> getDm_count() {
		return dm_count;
	}
	public void setDm_count(List<String> dm_count) {
		this.dm_count = dm_count;
	}
	
	

}
